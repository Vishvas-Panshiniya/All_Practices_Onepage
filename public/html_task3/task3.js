function leftside(){
    const left = document.getElementById("s6-img1");
    const right = document.getElementById("s6-img2");
    right.style.opacity = 1;
    left.style.opacity = 0.4;
}

function rightside(){
    const left = document.getElementById("s6-img1");
    const right = document.getElementById("s6-img2");
    right.style.opacity = 0.4;
    left.style.opacity = 1;
}